export function getSum(num1, num2){
    return num1 + num2;
}

export function getSub(num1,num2){
    return num1 - num2;
}

export function getMult(num1,num2){
    return num1 * num2;
}

export function getDivide(num1,num2){
    return num1 / num2;
}
export function getRemain(num1,num2){
    return num1 % num2;
}